//!
//! ## 核心模块
//!
//! | 模块 | 功能 |
//! |------|------|
//! | [`app::server`] | HTTP 服务器构建与启动 |
//! | [`app::database`] | 数据库连接池初始化 |
//! | [`app::common`] | 通用数据结构（分页等） |
//! | [`app::auth`] | JWT 认证（编解码 + 中间件） |
//! | [`app::validation`] | 自定义参数校验函数 |

